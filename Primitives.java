public class Primitives {
    public static void main(String[] args) {
        // Primitives
        int age = 9; // max size is 4 bytes
        // 9 is literals, age is identifiers
        // millions age = 7_000_42
        String name = "Jay";
        char letter = 's';
        float mark = 9.88f;
        double largeDecimalNumber = 80458405804.048504854850;
        long largeIntegerValue = 9493893l; // 8 bytes
        boolean flag = true;

        // Classes of data type
        Integer roll = 64; // Integer is wrapper class to add more functionality to primitive


    }
}
